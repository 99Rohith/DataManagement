    private void BtnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDelActionPerformed
        // TODO add your handling code here:
        conn = ConnectDB.Connect(dbPath);
        if(flag)
        {
            int reply = JOptionPane.showConfirmDialog(this, "Are you sure you want to Delete the record?");
            if(reply == JOptionPane.YES_OPTION)
            {
                try
                {
                    String query = "DELETE FROM records WHERE s_no = ?";
                    pst1 = conn.prepareStatement(query);
                    pst1.setString(1, squery);
                    pst1.execute();
                    TxtArea.setText("");
                    JOptionPane.showMessageDialog(this, "Record Deleted Successfully");
                }
                catch (SQLException ex) {
                    Logger.getLogger(DeleteRecord.class.getName()).log(Level.SEVERE, null, ex);
                }
                flag = false;
            }
            TxtS_no.requestFocus();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Press Enter after entering Record ID Field");
            TxtS_no.requestFocus();
        }
        ConnectDB.closeConnection(conn);
    }//GEN-LAST:event_BtnDelActionPerformed

