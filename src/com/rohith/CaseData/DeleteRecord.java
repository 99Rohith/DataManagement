    private void TxtS_noKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtS_noKeyPressed
        // TODO add your handling code here:
        conn = ConnectDB.Connect(dbPath);
        squery = TxtS_no.getText();
        flag = false;
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            flag = true;
            try
            {
                String query = "SELECT s_no,name_client,laop_no,appeal_no,"
                + "village,reach_no FROM records WHERE s_no = ?";
                pst = conn.prepareStatement(query);
                pst.setString(1, squery);
                rs = pst.executeQuery();
                if(rs.next())
                {
                    TxtArea.append("\nName of the Client : " + rs.getString("name_client"));
                    TxtArea.append("\nName of Village : " + rs.getString("village"));
                    TxtArea.append("\nReach No. : " + rs.getString("reach_no"));
                    TxtArea.append("\nLAOP No. : " + rs.getString("laop_no"));
                    TxtArea.append("\nAppeal No. : " + rs.getString("appeal_no"));
                    TxtArea.append("\n");
                }
                else
                {
                    flag = false;
                    TxtArea.setText("");
                    JOptionPane.showMessageDialog(this, "No Records found");
                }
            }
            catch (SQLException ex) {
                Logger.getLogger(DeleteRecord.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        ConnectDB.closeConnection(conn);
    }//GEN-LAST:event_TxtS_noKeyPressed

